package com.app.buna.boxsimulatorforlol.dto;

public class BoxResultData {

    private String champName, skinName, engChampName;
    private int num;

    public String getChampName() {
        return champName;
    }

    public void setChampName(String champName) {
        this.champName = champName;
    }

    public String getSkinName() {
        return skinName;
    }

    public void setSkinName(String skinName) {
        this.skinName = skinName;
    }

    public String getEngChampName() {
        return engChampName;
    }

    public void setEngChampName(String engChampName) {
        this.engChampName = engChampName;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

}
